let num = prompt('숫자를 입력하세요![1~100]')
let answer = Math.floor(Math.random() * 100) + 1


while (num!=answer){
    if(num>answer){
        alert(`down`)
    }
    else {
        alert(`up`)
    }
     
    num = prompt('숫자를 입력하세요![1~10]')
}
alert('정답입니다!')
