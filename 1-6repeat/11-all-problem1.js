// 입력창이고 1~100의 숫자를 고르게 만들어야함
//숫자를 입력하세요![1~100]

//숫자를 입력하면 컴퓨터에서 정해놓은 수에 맞게 UPDOWN 표시되어야 함

// 입력했으면 남은 기회 갯수 알려주면서 n번의 기회가 남았습니다

//입력한 수에서 UP이면 입력한 수부터 100까지
//입력한 수에서 DOWN이면 1부터 입력한 수 까지 출력 되어야 한다.

//컴퓨터에서 지정한 수와 입력한 수가 일치하면 정답입니다!

//정해진 횟수안에 맞추지 못하면 기회가 모두소진 되었습니다. 정답은 n번입니다.

